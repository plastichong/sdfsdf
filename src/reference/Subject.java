package reference;

public class Subject {
    private String name;
    private int scorePoint;



    public void setScorePoint(int scorePoint) {
        this.scorePoint = scorePoint;
    }

    public int getScorePoint() {
        return scorePoint;
    }

    public void setSubjectName(String name) {
        this.name = name;

    }
    public String getSubjectName() {
        return name;
    }
}
